$(document).ready(function(){

    function fetchClaimedAds() {
        $.ajax({
            url: "../ajax/ads.php",
            method: "GET",
            data: { action: "claimed" },
            dataType: "json",
            success: function(res){
                if(res.success && Array.isArray(res.claimed_ads)){
                    $(".btn[data-ad-id]").each(function(){
                        var adId = $(this).data("ad-id");
                        var expiredSpan = $(this).closest(".button-group").find(".expired-text");

                        var claimedAd = res.claimed_ads.find(a => a.ad_id === adId);
                        if(claimedAd){
                            expiredSpan.data("expiresAt", claimedAd.expires_at * 1000);
                            $(this).prop("disabled", true);
                        } else {
                            $(this).prop("disabled", false);
                            expiredSpan.removeData("expiresAt");
                        }
                    });
                }
            }
        });
    }

    function updateCountdowns(){
        $(".expired-text").each(function(){
            var expiresAt = $(this).data("expiresAt");
            if(expiresAt){
                var remaining = expiresAt - Date.now();
                if(remaining > 0){
                    var hours = Math.floor(remaining / 3600000);
                    var minutes = Math.floor((remaining % 3600000) / 60000);
                    var seconds = Math.floor((remaining % 60000) / 1000);
                    $(this).text(`Available in: ${hours}h ${minutes}m ${seconds}s`);
                } else {
                    $(this).text("Available now!");
                    $(this).closest(".button-group").find(".btn[data-ad-id]").prop("disabled", false);
                    $(this).removeData("expiresAt");
                }
            }
        });
    }
    fetchClaimedAds();

    setInterval(fetchClaimedAds, 5000);
    setInterval(updateCountdowns, 1000);


    $(".btn[data-ad-id]").on("click", function(){
        var btn = $(this);
        var adId = btn.data("ad-id");

        var expiredSpan = btn.closest(".button-group").find(".expired-text");

        $.ajax({
            url: "../ajax/ads.php",
            type: "POST",
            data: { action: "claim", ad_id: adId },
            dataType: "json",
            success: function(response){
                expiredSpan.text("");

                expiredSpan.text(response.message);

                if (response.expires_at) {
                    expiredSpan.data("expiresAt", response.expires_at * 1000);
                }

                btn.prop("disabled", true);
            },
            error: function(){
                alert("Error connecting to server.");
            }
        });
    });
});


$(document).ready(function () {

    function updateRefCount() {
        $.ajax({
            url: "../ajax/user-det.php",
            method: "GET",
            dataType: "json",
            success: function (response) {
                $(".active").text(response.active);
                $(".username").text(response.full_name);

                $(".ref-count .ref-number").remove();

                $(".ref-count").append(
                    $("<span>").addClass("ref-number").text(response.ref_number)
                );
            }
        });
    }
    updateRefCount();
    setInterval(updateRefCount, 2000);
});


